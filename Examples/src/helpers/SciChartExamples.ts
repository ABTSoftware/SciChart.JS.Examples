const generateExamplesGallery = () => {
    const galleryItems: GalleryItem[] = [];

    const generateGalleryItem = (menuItem: TMenuItem) => {
        galleryItems.push({
            chartGroupTitle: menuItem.item.name,
            items: menuItem.submenu.map(subMenu => {
                return {
                    imgPath: subMenu.thumbnailImage,
                    title: subMenu.title,
                    seoTitle: subMenu.pageTitle,
                    examplePath: subMenu.path,
                }
            })
        })
    };
    // Featured apps first
    MENU_ITEMS_FEATURED_APPS.forEach(generateGalleryItem);

    // 2D Charts
    MENU_ITEMS_2D.forEach(generateGalleryItem);

    // Then 3D Charts
    MENU_ITEMS_3D.forEach(generateGalleryItem);

    return galleryItems;
};
